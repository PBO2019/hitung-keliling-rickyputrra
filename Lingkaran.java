
public class Lingkaran {
    private int r;
    private double phi;
    private double keliling;


    public void hitunglingkaran() {
        this.keliling = 2 * phi;
    }

    public void setR(int r) {
        this.r = r;
    }

    public void setPhi(double phi) {
        this.phi = phi;
    }

    public double getKeliling() {
        return keliling;
    }
}
